public class Output {
    public static void main(String[] args) {
        // Output: Text
        System.out.println("Hello World!"); // Output: Hello World!

        // Output: Text
        System.out.print("Hi!");
        System.out.print("Test 123.\n");
        /*
            Output: Hi!Test 123.
        */

        // Output: Number
        System.out.println(123); // Output: 123
        System.out.println(1 + 1); // Output: 2
        System.out.println(5 * 2); // Output: 10
    }
}
